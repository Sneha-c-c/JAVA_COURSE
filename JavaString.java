public class JavaString {
    public static void main(String[] args){
    String name = new String("Sneha");
    String word = "Sneha";
    System.out.println(name); // It will print on newline 
    System.out.print(word);// it will print on same line 
    int a = 9;
    System.out.printf("The value of a is %d",a);// mainly used for formatting
    float b = 4.08939878f;
    System.out.printf("The value of b = is %f",b);
    System.out.printf("The value of b = is %.3f", b);
    System.out.printf("The value of b = is %7.3f", b);

    }
}
